// SmartContract provides functions for managing a FileRecord
type SmartContract struct {
	contractapi.Contract
}

// FileRecord represents a single file integrity event on the blockchain.
// Every security-critical field is explicitly tracked here.
type FileRecord struct {
	TxID                string `json:"tx_id"`                   // Fabric transaction ID
	FilePath            string `json:"file_path"`               // Canonical path on endpoint
	Blake3Hash          string `json:"blake3_hash"`             // High-throughput integrity hash
	Sha256Hash          string `json:"sha256_hash"`             // FIPS-compliant hash
	IpfsCID             string `json:"ipfs_cid"`                // Pointer to off-chain encrypted content
	DekKeyRef           string `json:"dek_key_ref"`             // HSM reference to DEK (NEVER the raw key)
	AgentID             string `json:"agent_id"`                // Registered edge agent identity
	TimestampNano       int64  `json:"timestamp_nano"`          // Event time from endpoint
	HSMSignature        string `json:"hsm_signature"`           // ECDSA-P384 signature over payload fields
	AttestationHash     string `json:"attestation_token_hash"`  // Binds event to agent binary integrity state
	ComplianceStateRoot string `json:"compliance_state_root"`   // Merkle root used for ZKP verification
	ComplianceStatus    string `json:"compliance_status"`       // COMPLIANT, NON_COMPLIANT, UNDER_INVESTIGATION
}

// SuspiciousFlag is appended by the AI Layer if an anomaly is detected
type SuspiciousFlag struct {
	OriginalTxID string  `json:"original_tx_id"`
	AnomalyScore float64 `json:"anomaly_score"`
	Explanation  string  `json:"explanation"`
	Timestamp    int64   `json:"timestamp"`
}

