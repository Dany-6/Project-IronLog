// LogFileChange records a new file change event to the ledger.
// Access Control: Only registered agent identities may invoke this function.
func (s *SmartContract) LogFileChange(
	ctx contractapi.TransactionContextInterface,
	filePath, blake3Hash, sha256Hash, ipfsCID, dekKeyRef, agentID string,
	timestampNano int64, hsmSignature, attestationHash string, zkpProof string) error {

	// -------------------------------------------------------------------------
	// 1. ACCESS CONTROL: Validate caller identity
	// -------------------------------------------------------------------------
	clientID, err := ctx.GetClientIdentity().GetID()
	if err != nil {
		return fmt.Errorf("failed to get client identity: %v", err)
	}
	
	// SECURITY CRITICAL: Verify client is authorized to write as agentID
	// Threat mitigated: T1078 (Valid Accounts) - Prevents agent spoofing
	isAuthorized, err := s.verifyAgentIdentity(ctx, clientID, agentID)
	if err != nil || !isAuthorized {
		return fmt.Errorf("unauthorized: client %s is not authorized for agent %s", clientID, agentID)
	}

	// -------------------------------------------------------------------------
	// 2. CRYPTO OP: HSM Signature Verification
	// -------------------------------------------------------------------------
	// Reconstruct payload string for signature verification
	payload := fmt.Sprintf("%s|%s|%s|%s|%s|%s|%d|%s",
		filePath, blake3Hash, sha256Hash, ipfsCID, dekKeyRef, agentID, timestampNano, attestationHash)

	// SECURITY CRITICAL: Verify the ECDSA-P384 signature generated by the agent's HSM
	// Threat mitigated: T1557 (Adversary-in-the-Middle) - Binds payload to hardware key
	validSig, err := s.verifyHSMSignature(ctx, agentID, payload, hsmSignature)
	if err != nil || !validSig {
		return fmt.Errorf("cryptographic verification failed: invalid HSM signature")
	}

	// -------------------------------------------------------------------------
	// 3. CRYPTO OP: Zero-Knowledge Proof (ZKP) Verification
	// -------------------------------------------------------------------------
	// Retrieve current compliance state (Merkle root of allowed hashes)
	currentRoot, err := s.getComplianceStateRoot(ctx)
	if err != nil {
		return fmt.Errorf("failed to retrieve compliance state root: %v", err)
	}

	// SECURITY CRITICAL: Verify ZKP (Groth16/PLONK) proof
	// Threat mitigated: Unauthorized file modification (T1565.001) passing validation
	isCompliant := s.VerifyCompliance(blake3Hash, currentRoot, zkpProof)
	
	status := "NON_COMPLIANT"
	if isCompliant {
		status = "COMPLIANT"
	}

	// -------------------------------------------------------------------------
	// 4. LEDGER COMMIT
	// -------------------------------------------------------------------------
	txID := ctx.GetStub().GetTxID()

	record := FileRecord{
		TxID:                txID,
		FilePath:            filePath,
		Blake3Hash:          blake3Hash,
		Sha256Hash:          sha256Hash,
		IpfsCID:             ipfsCID,
		DekKeyRef:           dekKeyRef,
		AgentID:             agentID,
		TimestampNano:       timestampNano,
		HSMSignature:        hsmSignature,
		AttestationHash:     attestationHash,
		ComplianceStateRoot: currentRoot,
		ComplianceStatus:    status,
	}

	recordJSON, err := json.Marshal(record)
	if err != nil {
		return err
	}

	// SECURITY CRITICAL: Check for duplicate CID to prevent replay attacks
	exists, err := s.cidExists(ctx, ipfsCID)
	if err != nil {
		return err
	}
	if exists {
		return fmt.Errorf("replay attack detected: CID %s already committed", ipfsCID)
	}

	// PutState commits the record to the ledger
	err = ctx.GetStub().PutState(txID, recordJSON)
	if err != nil {
		return fmt.Errorf("failed to put state: %v", err)
	}

	// -------------------------------------------------------------------------
	// 5. EVENT EMISSION
	// -------------------------------------------------------------------------
	// Emit indexed event for downstream consumption (AI Anomaly Detection / SIEM)
	// Threat mitigated: T1070 (Indicator Removal) - downstream consumers notified instantly
	err = ctx.GetStub().SetEvent("FileChangeRecorded", recordJSON)
	if err != nil {
		return fmt.Errorf("failed to set event: %v", err)
	}

	return nil
}

// FlagSuspiciousChange is invoked by the AI Anomaly Detection Middleware
// if it scores an event above the FP SLA threshold.
func (s *SmartContract) FlagSuspiciousChange(ctx contractapi.TransactionContextInterface, originalTxID string, anomalyScore float64, explanation string) error {
	// 1. Verify caller is the AI identity (omitted for brevity)
	
	// 2. Retrieve original record
	recordJSON, err := ctx.GetStub().GetState(originalTxID)
	if err != nil || recordJSON == nil {
		return fmt.Errorf("record not found: %s", originalTxID)
	}

	var record FileRecord
	err = json.Unmarshal(recordJSON, &record)
	if err != nil {
		return err
	}

	// 3. Update status
	record.ComplianceStatus = "UNDER_INVESTIGATION"
	updatedJSON, _ := json.Marshal(record)
	ctx.GetStub().PutState(originalTxID, updatedJSON)

	// 4. Save the flag
	flag := SuspiciousFlag{
		OriginalTxID: originalTxID,
		AnomalyScore: anomalyScore,
		Explanation:  explanation,
		Timestamp:    time.Now().Unix(),
	}
	flagJSON, _ := json.Marshal(flag)
	
	// Use a composite key for the flag so multiple flags can exist per tx
	flagKey, _ := ctx.GetStub().CreateCompositeKey("SuspiciousFlag", []string{originalTxID, fmt.Sprintf("%d", flag.Timestamp)})
	ctx.GetStub().PutState(flagKey, flagJSON)

	// 5. Emit alert event
	ctx.GetStub().SetEvent("SuspiciousChangeDetected", flagJSON)

	return nil
}

